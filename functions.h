int getSum(int a, int b) {
    return (a + b);
}

int getDifference(int a, int b) {
    return (a - b);
}


int getProduct(int a, int b) {
    return (a * b);
} 

float getQuotent(float a, float b) {
    return (a / b);
}

int getRemainder(int a, int b) {
    return a % b;
}


